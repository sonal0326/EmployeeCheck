package entity;

public class Employee {
	private String name;
	private int age;
	private String address;
	
	
	public String getAdd() {
		return address;
	}
	public void setAdd(String add) {
		this.address = add;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
